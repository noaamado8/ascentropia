Non obstante, a Asociación Sociocultural Entropía poderá realizar as comprobacións para verificar este feito, adoptando as correspondentes medidas de dilixencia debida, de acordo coa normativa de protección de datos.



Lexitimación: cal é a lexitimidade para o tratamento dos seus datos?
O tratamento dos datos que teñen como finalidade o envío de boletíns periódicos (newslettering) sobre servizos, eventos e novidades relacionadas coa nosa actividade profesional baséase no consentimento do interesado, solicitado expresamente para a realización de ditos tratamentos, de acordo coa normativa vixente.
Ademais, a lexitimidade para o tratamento de datos relacionados con ofertas ou colaboracións baséase no consentimento do usuario que envía os seus datos, que pode ser retirado en calquera momento, aínda que isto pode afectar á posible comunicación fluída e obstrución dos procesos. quero facer.
Por último, os datos poderán utilizarse para cumprir coas obrigas legais aplicables á Asociación Sociocultural Entropía

Período de conservación de datos: canto tempo conservaremos os teus datos?
A Asociación Sociocultural Entropía conservará os datos persoais dos usuarios só durante o tempo necesario para levar a cabo os fins para os que foron recollidos, sempre que non revogue os consentimentos outorgados. Posteriormente, se é necesario, manterá a información bloqueada durante os prazos legalmente establecidos.

Destinatarios A que destinatarios se comunicarán os teus datos?
Poderán acceder aos seus datos aqueles provedores que presten servizos á Asociación Sociocultural Entropía, como servizos de aloxamento, ferramentas de mercadotecnia e sistemas de contido ou outros profesionais, cando esa comunicación sexa legalmente necesaria ou para a execución dos servizos contratados.
A Asociación Sociocultural Entropía asinou os correspondentes contratos de orde de tratamento con cada un dos provedores que prestan servizos á Asociación Sociocultural Entropía, co fin de garantir que os ditos provedores tratarán os seus datos de acordo co establecido na lexislación vixente.
Tamén poden ser trasladados ás Forzas e Corpos de Seguridade do Estado nos casos en que exista unha obriga legal.
Bancos e entidades financeiras, para a recollida de servizos.
Administracións públicas con competencia nos sectores de actividade, cando o estableza a normativa vixente.

